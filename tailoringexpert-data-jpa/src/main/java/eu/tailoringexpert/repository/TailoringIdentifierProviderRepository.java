package eu.tailoringexpert.repository;

import eu.tailoringexpert.domain.ProjectEntity;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.Repository;
import org.springframework.data.repository.query.Param;

import java.util.function.BiFunction;

public interface TailoringIdentifierProviderRepository extends Repository<ProjectEntity, Long>, BiFunction<String, String, String> {


    @Query("Select t.identifier from #{#entityName} p inner join p.tailorings t where p.identifier=:project and t.name=:name")
    String apply(@Param("project") String project, @Param("name") String name);
}
